package Sept.ex_13_09_2024;

public class Task002_small_calculator {

    public static void main(String[] args) {
        double a = 34;
        double b = 10;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }

}
