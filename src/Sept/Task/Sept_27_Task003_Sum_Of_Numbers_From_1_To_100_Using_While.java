package Sept.Task;

public class Sept_27_Task003_Sum_Of_Numbers_From_1_To_100_Using_While {
    public static void main(String[] args) {

        //calculate the sum of the number from 1 to 100
        int sum = 0;
        int i = 1;
        while (i<=100){
            sum = sum + i;
            i++;

        }
        System.out.println(sum);


    }
}
