package September.ex_16_09_2024;

public class Lab037 {
    public static void main(String[] args) {
        // Relational operators = boolean
        // > < , , >= <= , != , ! -> true or false
        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int age_jeet = 25;
        int age_amit = 25;
        // boolean result = age_jeet > age_amit; // false
        boolean result = age_jeet >= age_amit;  //  false // false // age_jeet > age_amit or age_jeet = age_amit;
        System.out.println(result);


    }
}
